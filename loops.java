public class loops {
    public static void main(String[] args) {
        //while loop
        int i = 0;
        while(i < 5){
            System.out.println(i);
            i++;
        }

        int x = 10;
        while(x > 0){
            System.out.println(x);
            x--;
        }

        //do-while loop
        int m = 0;
        do { 
            System.out.println(m);
            m++;
        } while (m < 5);

        
        

    }
}