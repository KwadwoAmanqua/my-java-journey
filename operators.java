public class operators {
    public static void main(String[] args) {
        //operators
        //assignment operator.
        int x = 10;
        x+= 5;
        System.out.println(x);

        //comparison operator
        int a = 2;
        int b = 3;
        System.out.println(a>b);

        //logical operators
        int m = 5;
        int n = 6;
        System.out.println(m<6 && n>7);
    }
}
